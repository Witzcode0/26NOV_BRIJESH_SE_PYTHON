/*
In C, a data type is a classification that specifies which type of value a variable can hold. 

Primitive Data Types:

int: Integer data type represents whole numbers without any fractional part. 
For example, int x = 10;

int age = 27
int x = -20

float: Float data type represents floating-point numbers, which include decimal parts. 
For example, float y = 3.14;

float price = 30.5;
float marks = -23.5

double: Double data type is used for double-precision floating-point numbers, providing more precision than the float data type. 

For example, double z = 5.678;

char: Char data type represents a single character. 
For example, char ch = 'A';

char ch = 'b';

Non-Primitive Data Types:

Array: An array is a collection of similar data type elements. For example, int arr[5]; declares an integer array with 5 elements.

int marks[20] = {10,20,30,40,50,60,70,80}

Structure: A structure is a user-defined data type that allows grouping variables of different data types under a single name.
struct user {
    char name[20];
    int age;
    float weight;
}


Enumeration Data Type:

Enum: Enumerations allow you to create named constant values, making the code more readable. For example,
enum Days {
    Monday, 
    Tuesday, 
    Wednesday, 
    Thursday, 
    Friday, 
    Saturday, 
    Sunday
};


Void Data Type:

void: Void is used to indicate that a function does not return any value. For example, void myFunction();
*/